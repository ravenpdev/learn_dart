void arithmeticOperators() {
  int sum = 5 + 5;
  print(sum);

  int difference = 5 - 2;
  print(difference);

  int product = 3 * 3;
  print(product);

  double quotient = 9 / 1.5;
  print(quotient);

  // ~/ integer division
  int remainder = 10 ~/ 3;
  print(remainder);

  // / double division
  double remainderDouble = 10 / 3;
  print(remainderDouble);
}
